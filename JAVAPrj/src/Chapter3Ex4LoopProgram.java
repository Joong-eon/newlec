
public class Chapter3Ex4LoopProgram {

	public static void main(String[] args) {
		/*
		 * int i = 0; 
		 * while(i<10) {
		 * 반복하는 코드블록
		 * i++; }
		 */

		for(int i=0; i<10; i++) {
			//1. 1~10
			//1번 답 : System.out.println(i+1);
			//2. 10~1
			//2번 답 : System.out.println(10-i);
			//3. 1~5, 1~5
			//4번 답 : System.out.println((i%5)+1);
			
		}
	}

}
