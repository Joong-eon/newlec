package chapter7ex4_3;

public class Omok {
	private int x,y;
	private int color;
	
	public Omok() {
		x = 0;
		y = 0;
		color = 0;
		
		
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}
}
